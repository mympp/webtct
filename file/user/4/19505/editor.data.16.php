<div><b>【产品说明】</b></div>
<div><span>&nbsp;&nbsp;&nbsp;&nbsp; </span>生物指示剂培养器是采用自动控温加热系统的生物指示剂专用配套设备，适用于压力蒸汽灭菌器生物指示剂灭菌效果的检测培养。</div>
<div>性能指标：</div>
<div>1.温度范围：室温<span>+5℃ ~ 80℃ </span></div>
<div>2.温度均匀性（<span>@56℃下检测）： &le;&plusmn;0.3℃&nbsp;</span></div>
<div>3.控温精度：&plusmn;<span>0.3℃</span></div>
<div>4.温度显示精度：<span>0.1℃</span></div>
<div>5.升温速度：<span>&lt;5min (从20℃ 到 56℃)</span></div>
<div><b>【</b><b>产品特点</b><b>】 </b></div>
<div>1、<span>LCD液晶显示</span></div>
<div>2、即时温度显示，时间递减显示</div>
<div>3、自动故障检测及蜂鸣器报警功能</div>
<div>4、自动控温加热系统，无明火，寿命长</div>
<div>5、设有定时功能，<span>0~100小时范围内任意设定培养时间</span></div>
<div>6、培养器内温度为<span>56℃，一次能容纳15支生物指示剂小管</span></div>
<div>7、多种标准样品模块可供选择，更换方便。</div>
<div>8、温度偏差校准功能</div>
<div>9、平均功率<span>10W，功耗低</span></div>
<div>10、多重安全保护功能，符合<span>CE 安全标准，安全可靠</span></div>
<div>11、模块孔径可根据试管大小定制</div>
<div>12、尺寸小：<span>110mm&times;148mm&times;120mm，重量仅0.5KG</span></div>
<div><b>【</b><b>用途</b><b>】</b></div>
<div><span>&nbsp;&nbsp;&nbsp; </span>适用于压力蒸汽灭菌器生物指示剂灭菌效果的检测培养。</div>
