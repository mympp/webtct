<p>五常稻花香大米是黑龙江五常市种植的一种稻花香水稻生产的大米。由于水稻成熟期产区昼夜温差大，五常米中可速溶的双链糖积累较多，对人体健康非常有益。其品质上乘，米粒均匀，色泽光亮，醇厚绵长，芳香四溢。五常稻花香大米成饭食味清淡略甜，绵软略黏，芳香爽口，饭粒表面油光艳丽，剩饭不回生。市场上很难买到纯正的五常稻花香大米，基本都兑有其他的大米，一般来说，能买到90%以上纯度的，就很不错。有些比例只有二三十。价格也有很大的差距。</p>
<p>食用建议：最好采用专门的电饭煲。如果有时间，最好在做饭前，将大米浸泡半小时。或者，饭做好后，将其打散，再焖十分钟左右，也会好吃很多。另外，在米饭里滴几滴油，最好是稻米油，也可以让米饭变得更好吃。不仅仅是五常稻花香大米，其他大米也如此。</p>
