&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;国庆节，是粮油一年中的传统消费旺季。粮、油，是生活中不可或缺之物。大米是主食，小米是五谷杂粮之首，橄榄油则是油中贵族。虽然进入秋季，人们更看重&ldquo;贴秋膘&rdquo;，但专家也提醒，除了适当补充肉类之外，作为主食部分的粮油也应当考究，高品质的粮油是营养三餐的基础，同样需要精心挑选。</p>
<p>今年新米价格略低于去年</p>
<p>&ldquo;新米好吃&rdquo;早已是众人皆知的事实。从本月上旬开始，东北主产区早熟稻开始收割并少量上市，这意味着新粮收获季即将全面展开。</p>
<p>北京锦绣大地玉泉路市场信息部主任刘敬亮介绍，近期，东北新大米已开始上市销售。此批大米产地为黑龙江五常地区，属于长粒大米。由于是早熟稻，且并未开始大面积收割，上市量较小。但与往年有所不同的是，今年上市初期价位明显低于往年水平，此外新陈米的价差也进一步缩小。据市场上销售新米的商家介绍，新上市长粒大米每袋(50斤)批发价格在142元左右，前两年价位都在150元以上，明显便宜了十多块钱。此外，目前同一品牌去年产大米价位在140元左右，新陈米价差也缩小至两三块钱。</p>
<p>大连生威集团董事长赵毅介绍，真正的新米，只能在农民家里吃到，水分充足，香气扑鼻，非常好吃。但在市场上买到的大米，即使是今年收割的，也不算是真正的新米。因为市场上销售的大米，为了安全和美观，牺牲了口感。</p>
<p>赵毅说，新米水分充足，不耐储存，为了安全，增加保质期，需要对大米进行脱水干燥处理；另外，在大米与稻壳之间，还有一层糊粉层，含有大量油脂，可以让米饭变得香甜，但颜色不好看，也不耐储存，所以，为了美观，需要对大米进行抛光处理。以前淘米水很浑浊，就是因为大米上还有残留的糊粉层。现在市场上卖的大米，又白又亮，还能放18个月，但已经不好吃了。</p>
<p>中国农业科学院稻米质量安全评估创新团队首席朱智伟研究员介绍，新大米上市的时间比较长，即使不储存，现收的米，从收割到上餐桌的时候，差不多也要两三个月，因为要晒干入库，再从仓库调到米厂，再加工成米卖给消费者，得有两三个月。</p>
<p>&ldquo;保健大米&rdquo;更多是营销策略</p>
<p>近几年，市场上还出现各种保健大米，如&ldquo;高钙富硒大米&rdquo;、&ldquo;竹营养米&rdquo;，&ldquo;麦饭石大米&rdquo;等。朱智伟表示，市场上的保健概念米，是市场营销的一个策略，但从实际保健疗效来讲，没有相应的动物实验，或者其他什么实验来相对证明，就很难去说这个问题。赵毅表示，大米的成分主要是淀粉，提供碳水化合物，让人吃了后有饱腹感。即便含有其他保健元素，也极其微小，可以忽略不计。大米就是主食，不可能变成保健品。</p>
<p>橄榄油、小米价格略有上涨</p>
<p>从以往数据来看，国庆是食用油市场一年当中仅次于元旦、春节的黄金销售期。但北京锦绣大地玉泉路批发市场提供的食用油价格数据可以看到，除了橄榄油、花生油近期有一定涨幅外，大豆油、调和油和菜籽油等食用油产品的价格均有所下滑。</p>
<p>刘敬亮介绍，目前全国范围内，食用油价格都有大幅下滑。进入9月，北半球大豆新作即将收获上市，美国大豆丰收前景及国内豆粕供货充裕对豆油价格形成进一步压制。预计在供应总体宽松的背景下，食用油价格仍将继续弱势运行。</p>
<p>不过，全球橄榄油供应紧张，价格趋于上涨，因为头号生产国和出口国西班牙的橄榄油减产。西班牙橄榄主产区安达卢西亚地区降雨不足，加上橄榄树经过上年创纪录的产量后进入低产期，因而导致西班牙橄榄油减产。同样价格走高的还有小米。由于去年小米种植面积减少，产量下降导致近一年以来小米行情一路攀升。在6、7月份持续平稳运行两个月后，近日小米行情有所反弹。</p>
<p>刘敬亮表示，去年总产量有所减少，加上近一年的不断消耗后，市场供给能力明显下降，青黄不接时期使得近日小米价格再次小幅上调。不过，新季小米一个月后将陆续收获并上市，因此当前小米价格上涨空间十分有限。</p>
