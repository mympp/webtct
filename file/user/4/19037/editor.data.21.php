&nbsp;&nbsp;&nbsp;&nbsp; 为提高药品和医疗器械安全突发事件的快速反应和应急处置能力，9月26日，我区在东兴市举行药品和医疗器械安全不良事件监测应急演练。</p>
<p><br />
本次的应急演练采取实地演练的形式，为了一场涉及到人数超过两人、少于3人的Ⅳ级药品及医疗器械安全突发事件的应急演练。现场模拟东兴市人民医院血透室使用的新进透析器过程中，两名患者出现了严重不适症状，在出现了突发事件后，立即启动了应急机制，在经过各相关部门组成的应急指挥部的统一指挥下，各部门协调配合，顺利地完成医疗器械不良反应监测事件应急预案的启动、调查处理和善后处置的过程。近年来，尤其是今年6月1日新版《医疗器械监督管理条例》实施以来，像这样的针对医疗器械不良事件的正式、大规模演练在全区范围内尚属首次。</p>
<p>自治区食药监局相关负责人介绍，此次演练突出了问题导向、操作规范、处置科学的特点，为我区医疗器械不良事件监测创建了示范和模板，具有很强的指导意义。</p>