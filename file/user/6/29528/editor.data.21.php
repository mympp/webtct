	南都讯 记者阳广霞 通讯员粤卫信 民营医疗机构人才匮乏、申办障碍重重，政府规划设置“关卡”……昨日，省政府发布《广东省促进社会办医加快发展实施方案》(简称方案)，<span style="color:#00D5FF;"><strong>鼓励医护人员开诊所、办护理机构，成立医生集团，放宽准入制度，精简审批环节。方案明确力争到2018年，社会办医疗机构床位和诊疗服务量均占总量三成左右。</strong></span> 
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	鼓励成立医生集团
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	以往，医务人员无法在不同类型、不同层次医疗机构之间流动，民营医疗机构人才匮乏，是制约社会办医疗机构发展的重要因素。此次方案鼓励医疗机构建立全职、兼职聘用制度，推进医务人员从“单位人”向“社会人”转变，鼓励兼职执业医师开办诊所、中医馆、中医坐堂医诊所，鼓励兼职执业护士开办护理机构。也鼓励成立医生集团等医疗人力资源管理机构。
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	方案还透露，要在全省范围内实行所有类别的医师及护士第一执业地点报备制、省域注册制、多点执业注册网络备案制。医务人员在学术地位、职称晋升、业务培训等方面不受多点执业影响。
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	扩大社会办医规模
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	一直以来，规划设置是制约社会办医的重大障碍。方案要求扩大社会办医规模，合理控制公立医疗机构的数量、规模，明确力争到2018年社会办医疗机构床位和诊疗服务量均占总量30%左右，相比之前的目标提高了5个百分点。而且，规划不对非公立医疗机构的类别、规模、数量、布局等进行限制。
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	方案也要求，拓展社会办医发展空间，每千常住人口公立医疗机构床位超4张的地级以上市，原则上应统筹将增量空间主要留给社会办医疗机构。据省卫计委介绍，到去年年底全省民营医疗机构达2 .2万家，占全省医疗机构总数的46%。民营医院与公立医院一样具有很强竞争力的大型三级甲等综合性医院，例如东莞市康华医院、东华医院、佛山市禅城区中心医院。但是，民营医疗机构的床位、服务量与其数量不相称。其中，床位数5 .7万张，占全省床位总数的13%，民营医疗机构总诊疗人次、出院人次占比为21%、9%，与30%的目标要求仍然存在较大差距。
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	纳入政府补助范围
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	此前，申办社会办医疗机构障碍重重，时间周期长，部门条条框框多，手续繁琐，方案也要求按照“非禁即入”的原则，全面清理、取消各种不合理的前置审批事项，明确并缩短审批时限，简化审批流程。此外，下放审批权限，大部分社会办医疗机构设置均实行属地化审批管理，取消逐级申请、逐级审批，申请人直接向审批机关提交设置申请。
</p>
<p style="font-size:16px;font-family:宋体, Arial, sans-serif;background-color:#FFFFFF;">
	方案也在民营医疗机构准入方面进一步放宽条件。鼓励社会力量举办康复医院、老年病医院、护理院和高端医疗服务机构，允许医师在符合条件的药店开办诊所等。在财政扶持方面，各地可将社会办非营利性医疗机构按规定纳入政府补助范围，在临床重点专科建设、人才培养等方面，执行与公立医疗机构同等补助政策。
</p>
