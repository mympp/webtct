<p>&nbsp;</p>
<p style="text-align: center;"><span style="font-size: 20px;"><strong><strong style="font-size: 20px; text-align: center; white-space: normal;">听见流星的声音</strong></strong></span></p>
<p style="text-align: center;"><span style="font-size: 16px;">梁朝伟</span></p>
<p>&nbsp;</p>
<p>2012年初，《大魔术师》上映。之后的三年里，我和尔冬升各忙各的，几乎再没见过面。</p>
<p>&nbsp;</p>
<p>这三年中，我听说这位老友跑去横店拍了一部和群众演员有关的电影，叫作《我是路人甲》。当时最令我感到好奇的，其实不是他为什么要去拍路人甲，而是他要怎么拍。这个一出道就当男主角，才貌双全又很任性的大个子，他从来都没做过路人甲，他要怎么去拍出路人甲的人生？</p>
<p>&nbsp;</p>
<p>就是这样一个人，最近突然找到我，说要请我看电影。于是，我有幸提前看到了那部传说中的《我是路人甲》。</p>
<p>&nbsp;</p>
<p>这是一次很意外的观影经历，如果要用一个词来概括的话，我会想到&ldquo;舒服&rdquo;。对，这是我今年看过的最舒服最清新的一部片，也是我最喜欢的类型，有传递信息，会让人思考，无论笑或哭都发自内心，毫不勉强。</p>
<p>&nbsp;</p>
<p>尔冬升好像变了，变成熟了。</p>
<p>&nbsp;</p>
<p>我想他听到这句话可能不一定很开心。</p>
<p>&nbsp;</p>
<p>时光流转回2001年，那一年，我去横店拍《英雄》。</p>
<p>&nbsp;</p>
<p>那是我第一次去横店，当时天气转凉，还没有那么多人，我每天踩着单车去片场，收工之后也会骑车到处转转，无聊时还会买些烟花，然后找个空旷的地方放一放。横店的夜晚很静，放烟花的时候会吓到一些乡亲大叫，我躲起来偷笑，假装与我无关。</p>
<p>&nbsp;</p>
<p style="text-align: center;"><img data-s="300,640" data-type="png" src="http://www.tecenet.com/file/upload/weixin/image/r7l7L28KwuWU6rlbC8yM38r9KJ2Nahl1N27KHsde9gutUdYGu9p3bhHHXfsoOfHKVevHtmRw6gB7EeVZ7ygjoQ.png#" data-ratio="0.5621414913957935" data-w="" style="text-align: center; white-space: normal;" alt="" /><br style="text-align: center; white-space: normal;" />
<span style="text-align: center;  font-size: 12px; color: rgb(127, 127, 127);">《英雄》剧照</span></p>
<p>&nbsp;</p>
<p>那年的11月19号，新闻说晚上会有狮子座流星雨。半夜收工后，我拖着导演还有组里的人跑去片场的楼顶很兴奋地等着。突然，一颗流星划过，然后，又一颗。起初我们都会开心地欢呼，接下来就被越来越多的流星吓到，每个人都不再说话。</p>
<p>&nbsp;</p>
<p>在这样安静的环境里，我隐约地听到了&ldquo;咻咻&rdquo;的声音，起初我有些奇怪那是什么声音，后来随着&ldquo;咻&rdquo;的一声，又一颗流星划过头顶，那时候我才发现，原来是流星划过天空的声音。</p>
<p>&nbsp;</p>
<p>在那之前，我从来不知道流星也会有声音，城市里太嘈杂，人人都很忙，没时间听流星说话。</p>
<p>&nbsp;</p>
<p>因为那场流星雨，我至今对横店都留有很美好的印象。</p>
<p>&nbsp;</p>
<p>时光流转回30年前，当时的我还在卖家用电器，生活无风无浪。那时候我对未来的唯一设想就是，如果没意外的话，大概会一直升职到销售经理吧。</p>
<p>&nbsp;</p>
<p>这样其实也没什么不好，但偶尔会觉得，这似乎不是我要的生活，至于我到底要什么，那时候的我并不知道。</p>
<p>&nbsp;</p>
<p>多亏一位老友，那段时间一直给我洗脑，每天给我画各种光怪陆离的蓝图，劝我放弃工作和他一起去考艺员训练班，我最后被他说动，于是迈出了那一步。</p>
<p>&nbsp;</p>
<p>我很感谢那位老友，但我妈当时很生气，因为她觉得这个叫周星驰的家伙害他儿子辞掉了稳定的工作，去上什么前途未卜的培训班，我至今都记得她当时对我说的那句话：&ldquo;衰仔！我一块钱都不会给你！&rdquo;</p>
<p>&nbsp;</p>
<p>她真的是讲得出就做得到，在艺员训练班的那一年，是我靠自己之前的积蓄撑下来的。那一年，我每天出门只带十块钱，走路去上课。如果不小心起晚了，十块钱就要交给的士司机，那天便只能捱饿。</p>
<p>&nbsp;</p>
<p style="text-align: center;"><img data-s="300,640" data-type="png" src="http://www.tecenet.com/file/upload/weixin/image/r7l7L28KwuWU6rlbC8yM38r9KJ2Nahl1M4AHWRLtaZ7sYsOzuCMVFtoITYazUHRmABIdYlF88b2usfauHiazZeQ.png#" data-ratio="0.6328871892925431" data-w="" style="text-align: center; white-space: normal;" alt="" /><br style="text-align: center; white-space: normal;" />
<span style="text-align: center;  color: rgb(127, 127, 127); font-size: 12px;">梁朝伟与周星驰</span></p>
<p>&nbsp;</p>
<p>我很怀疑我有没有对尔冬升讲过那段经历，因为，电影里那个叫万国鹏的男孩，和当年的我，真的是一模一样的境遇。不单是他，戏里的每一个路人甲，从初入行时的不知所措，到演戏时的每一次用力过猛，都会让我忍不住笑出声，就好像看到30年前的自己。</p>
<p>&nbsp;</p>
<p>唯一的区别，是他们带着明确的目标去了横店，而我是在进入训练班之后，才发现这是我想要的人生&mdash;&mdash;我想要成为一名演员，不是明星，不是影帝，就只是演员。</p>
<p>&nbsp;</p>
<p>&ldquo;演员&rdquo;这个词对我而言分量太重。2013年，我在L.A工作的时候接触到一些&ldquo;临时演员&rdquo;，他们平时都在从事各种各样的职业，有的是侍应，有的是清洁工，但当你问他们是做什么的时候，他们还是会说：&ldquo;我是个演员。&rdquo;</p>
<p>&nbsp;</p>
<p>我相信，能说出&ldquo;我是演员&rdquo;这句话的人，对演戏一定是有热情的。对我来说，演员的工作就是无条件把戏做好，无关其他。</p>
<p>&nbsp;</p>
<p>这些年来，经常有人问我，如果不做演员会做什么？我至今都想不出这个问题的答案。我不会做别的，从我入行那天起，就有一个强烈的执念伴随我，就是：不管我的角色是什么，戏份有多少，哪怕只给我一秒钟的镜头，我也要想办法让你在这一秒钟内记住我。</p>
<p>&nbsp;</p>
<p>为了实现这个执念，我努力练习了很久，很难说这个执念就是我最初的&ldquo;梦想&rdquo;，但我要对得住&ldquo;演员&rdquo;这两个字，就必须做到这一点。</p>
<p>&nbsp;</p>
<p>在片场的时候，我会常常忘记我是梁朝伟，因为我只记得那个执念，到今天也是这样。</p>
<p>&nbsp;</p>
<p><img data-s="300,640" data-type="png" src="http://www.tecenet.com/file/upload/weixin/image/r7l7L28KwuWU6rlbC8yM38r9KJ2Nahl1fm0mUKBicnmjuGwvECOnQQsbpOLicaDnrmKMedIg4U0UN8tyGbhBjQtw.png#" data-ratio="0.5602294455066922" data-w="" alt="" /></p>
<p>&nbsp;</p>
<p>回到眼前。我最近在家里看了很多日本电影，尤其是染谷将太主演的几部电影我很喜欢。看《我是路人甲》的时候，也好几次有种错觉，那个叫万国鹏的男孩和染谷将太有些相似，懵懂的样子在无形中化解了故事本身的压力，令到观影过程也变得轻松愉悦起来。</p>
<p>&nbsp;</p>
<p>是的，《我是路人甲》这故事本身并不轻松，主演亦都是陌生稚嫩的面孔，但尔冬升太聪明，他很清楚，要完成这个题材，唯一的办法，就是找真正的路人甲来演。对演员抱持着清楚的认知和无比信任的态度，是他自《癫佬正传》开始树立起来的风格，我一直记得他看演员的眼神，那眼神分明就是在说：对，我没看错，你就是这样的人。</p>
<p>&nbsp;</p>
<p>这感觉有时候很讨厌，但他偏偏总是对的。</p>
<p>&nbsp;</p>
<p>戏里，年轻的路人甲们在探讨何为成功，我看的时候也在思考。我理解的成功，不是衣食无忧，不是获奖无数，而是你能否真正享受每一次努力的过程。有梦想有目标是好事，但如果只看到目标，就很容易忽略过程，就像跑步一样，你一心想跑到终点，就会忘记欣赏沿途的风景。</p>
<p>&nbsp;</p>
<p>所以，我们有时不懂珍惜，有时自视过高，有时怨天尤人，其实说到底，都是放不下自我。很多心中不平都因为放不下，当我们学会放下，往往会获得更多。</p>
<p>&nbsp;</p>
<p>梦想，不仅仅是有梦、敢想，还有做梦和思考的过程。因为有了这个过程，所以，结果是什么，也没那么重要了。</p>
<p>&nbsp;</p>
<p>在大多数人看来，路人甲只是路人甲，就像偶尔擦过夜空的流星，不会一直停留在你的生命里。</p>
<p>&nbsp;</p>
<p>但是，即使微弱如流星，也会有它的轨迹，也会在夜深人静时，借着划过夜空的那一秒钟，发出属于它自己的声音，希望被有心的人听到。</p>
<p>&nbsp;</p>
<p>我想，路人甲也是一样，在默默坚持了那么久之后，终于遇到了那个叫尔冬升的人。</p>
<p>&nbsp;</p>
<p>这一次，希望有更多人听见流星的声音，哪怕只有一秒。</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>2015年5月24日</p>
