<p><span style="color: rgb(192, 0, 0);"><strong>职业：</strong></span>中国香港演员</p>
<p>&nbsp;</p>
<p><span style="color: rgb(192, 0, 0);"><strong>当选原因：</strong></span>2015年5月，电影《我是路人甲》在香港举行了一场小型试映会。活动结束之后，有一位观众欣然写了一篇观后感，然后发给了尔冬升导演。这位观众的名字叫做：梁朝伟。</p>
<p>&nbsp;</p>
<p>文中，梁朝伟对尔冬升新片《我是路人甲》颇多赞美之词，并回顾了自己的演艺之路，文辞朴素却精致。众网友纷纷点赞梁朝伟的文笔，更有网友表示这是梁朝伟的高考满分作文。</p>
<p>&nbsp;</p>
<p>梁朝伟写道：&ldquo;这些年来，经常有人问我，如果不做演员会做什么？我至今都想不出这个问题的答案。我不会做别的，从我入行那天起，就有一个强烈的执念伴随我，就是：不管我的角色是什么，戏份有多少，哪怕只给我一秒钟的镜头，我也要想办法让你在这一秒钟内记住我。&rdquo;</p>
