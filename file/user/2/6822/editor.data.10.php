<div>&nbsp; &nbsp; &nbsp; &nbsp;三月春暖花开，外出踏青的人多了，皮肤过敏也进入了一个高发期。以安徽中医院皮肤科门诊量为例，皮肤过敏的患者比上个月多出了两成。在这场春季皮肤保卫战中，除了传统的紫外线增强、花粉等顽固势力外，灰尘成为正在迅速壮大的一股势力，日益成为爱美女性的大敌。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp;昨天10时20分，安徽中医院皮肤科诊区走进一位年轻女子，用口罩遮住了半张脸。直到坐到医生面前，女子才摘下口罩，露出两侧脸颊上的成片红疹。&ldquo;这已经是近一个月来第二次发作了。&rdquo;女子说，以前也没有出现过皮肤过敏的症状。&ldquo;你这可能是对灰尘过敏。&rdquo;皮肤科主治医师王建锋说，这已是他上午看的第三个疑似灰尘过敏的病人了。</div>
<div>&nbsp;</div>
