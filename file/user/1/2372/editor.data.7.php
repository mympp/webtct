<p>LIS系统产品功能模块:</p>
<p>1、&nbsp;检验工作站：是LIS最大的应用模块，是检验技师的主要工作平台。负责日常数据处理工作，包括标本采集，标本数据接收，数据处理，报告审核，报告发布，报告查询等日常功能。</p>
<p>2、&nbsp;医生工作站：主要用于病人信息浏览、历史数据比较、历史数据查询等功能。使医生在检验结果报告出来之后可第一时间得到患者的病情结果，并可对同一个病人的结果进行比较，并显示其变化曲线。</p>
<p>3、&nbsp;护士工作站：具有标本接收、生成回执、条码打印、标本分发、报告单查询、打印等功能。</p>
<p>4、&nbsp;审核工作站：主要的功能是漏费管理的稽查，包括仪器日志查询分析、急诊体检特批等特殊号码的发放及使用情况查询与审核、正常收费信息的管理等功能。该功能可以有效控制人情检查和私收费现象。</p>
<p>5、&nbsp;血库管理：具有血液的出入库管理，包括报废、返回血站等的处理。输血管理，包括申请单管理、输血常规管理、配血管理、发血管理等功能。</p>
<p>6、&nbsp;试剂管理子系统：具有试剂入库、试剂出库、试剂报损、采购定单、库存报警、出入库查询等功能。</p>
<p>7、&nbsp;主任管理工作站：主要用于员工工作监察、员工档案管理、值班安排、考勤管理、工资管理、工作量统计分析、财务趋势分析等。</p>
<p>产品特色：</p>
<p>★&nbsp;条形码处理方式全流程的支持，打造全自动生化实验室。</p>
<p>★&nbsp;开放的接口架构，与医院HIS系统无缝联结。</p>
<p>★&nbsp;特有的漏费管理流程，避免小收费大检查现象，坚决杜绝漏费情况。</p>
<p>★&nbsp;财务趋势分析，工作量分析统计，准确了解实验室收支状况。</p>
<p>★&nbsp;试剂分析，通过采购申请、采购出库、申请出库、月和季盘点、月和季统计等各个环节实时掌握试剂消耗情况。独有的库存量预警功能，杜绝试剂零库存风险。</p>
<p>★&nbsp;仪器管理，详细记录检验仪器工作状态、维护日志、检修日志等信息。</p>
<p>★&nbsp;实时工作监察，准确掌握每一个检验医生、每一台检验设备的工作状态。</p>
<p>★&nbsp;值班安排、考勤管理、工资管理等模块使主任工作管理得心应手。</p>
<p>★&nbsp;严格的权限控制，灵活的权限分配方法，支持以角色的形式来定义不同的用户权限。</p>
<p>★&nbsp;协助建立独立的采样中心，改善采样环境，通过排队叫号系统缩短病人采样时间。</p>