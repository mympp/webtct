<?php exit;?>2013-10-11 13:49:31<p>国家卫生计生委10日就践行群众路线服务百姓健康工作举行发布会，国家卫生和计划生育委员会新闻发言人邓海华在谈到有关&ldquo;俄罗斯全民免费医疗&rdquo;报道时表示，医改是世界性难题，各国情况都不同。</p>
<p><br />
近期有媒体报道，俄罗斯宣布要实行全民免费医疗，消息在中国网络引起了很多关注。邓海华对此表示，对俄罗斯的医疗卫生制度不做评论，因为各个国家都不同。目前也有文章提出，国内媒体对这条消息存在误判。</p>
<p><br />
邓海华介绍，中国医改从2009年启动的新一轮医药卫生体制改革，经过这几年的工作取得了重大的阶段性成效，尤其是在医疗保障制度方面，实行了全民医保。虽然全民医保制度有了，但保障水平在今后工作中还需要进一步不断提升，真正做到全体居民病有所医。2009年中共中央国务院发布的文件提出，到2020年基本建立覆盖城乡居民的基本医疗卫生制度，实现人人享有基本医疗卫生服务。届时将以基本医疗保障制度为主，其他的商业医疗保障制度为补充，慈善、救助等共同构成整体的国家医疗保障体系。</p>