<p class="MsoNormal" align="left">
	<b>7.</b><b>办理退款的手续</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	当事人对多缴、重缴、错缴的专利费用，可以自缴费之日起一年内，提出退款请求。提出退款请求的，应提交意见陈述书，并提交《国家知识产权局专利收费收据》复印件。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b>8.</b><b>专利费用的减缓</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	申请人或者专利权人缴纳专利费用确有困难的，可以请求减缓。可以减缓的费用包括五种：申请费（其中印刷费、附加费不予减缓）、发明专利申请审查费、复审费、发明专利申请维持费、自授予专利权当年起三年的年费。其他费用不予减缓。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	请求减缓专利费用的，应当提交费用减缓请求书，如实填写经济收入状况，必要时还应附具有关证明文件。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b> </b><b>9.</b><b>年费的缴纳</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b> </b>(1) 申请人办理登记手续时，应当缴纳专利登记费、公告印刷费、印花税和授予专利权当年的年费。发明专利申请需要缴纳申请维持费的，申请人应当一并缴纳各个年度的申请维持费。期满未缴纳或未缴足费用的，视为未办理登记手续。授予专利权当年的年费，应当在专利局发出的授予专利权通知书中指定的期限内缴纳， 以后的年费应当在前一年度期满前一个月内预缴。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	(2) 需要缴纳申请维持费的情况是，发明专利申请自申请日起满两年尚未被授予专利权的，申请人应当自第三年度起缴纳申请维持费。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b>10.</b><b>滞纳金的缴纳</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b> </b>(1) 专利权人未按时缴纳授予专利权当年以后的年费或者缴纳的数额不足的，专利权人自应当缴纳年费期满之日起最迟六个月内补缴，同时缴纳滞纳金。交费时间超过规定交费时间不足一个月的，不收滞纳金，超过规定缴费时间一个月的，每多超出一个月，加收当年全额年费的5%作为滞纳金，例如，缴费时超过规定缴费时间两个月，滞纳金金额为年费标准值乘以10％（专利法实施细则第96条）。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	(2) 首次缴纳数额不足时年费滞纳金的计算：专利权人再次补缴时，应依照实际补缴日所在滞纳金时段内的滞纳金标准，补足应缴纳的全部年费滞纳金。例如，年费滞纳金5%的缴纳时段为5月5日至6月5日，滞纳金为45元，但缴费人仅缴纳了25元。缴费人在6月7日补缴滞纳金时，应依照实际缴费日所对应的滞纳期时段的标准10%缴纳，该时段滞纳金为90元，所以缴费人还应补缴滞纳金65元。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	(3) 办理恢复手续时年费滞纳金的计算：专利权人因专利权终止办理恢复手续时，年费滞纳金应按当年年费全额的25%缴纳。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b>11.</b><b>年费的减缓</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b> </b>授权前已获准专利费用减缓的，自授权当年起连续三个年度可按已批准的减缓比例缴纳年费。例如，一件已获准减缓专利费用的专利申请的授权当年为第三年度（即办理登记手续通知书中所指明的年度），则专利权人按批准的减缓比例可以减缓第三年度、第四年度及第五年度的年费，第六年度起应按全额缴纳年费。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b>12.“</b><b>专利年度</b><b>”</b><b>概念</b>
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	<b> </b>(1) 授予专利权当年：是指专利申请在第N年度授权，第N年即为授权当年，以办理登记手续通知书中所注明的为准。例如，一件专利申请的办理登记手续通知书中要求缴纳第三年度的年费，即表示该案的“授权当年”为第三年度。
</p>
<p class="MsoNormal" align="left">
	&nbsp;
</p>
<p class="MsoNormal" align="left">
	(2) 专利年度：是指该专利自申请日起每满一年为一个专利年度。例如，一件专利申请的申请日为1998年6月1日，则自该日起至1999年6月1日为该案的第一年度，自1999年6月2日至2000年6月1日为该案的第二年度……第N年度。
</p>
