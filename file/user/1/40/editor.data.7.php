<div>四、性传播疾病</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 常见的性传播疾病包括梅毒、淋病、非淋菌性尿道炎(或宫颈炎)、软下疳、性病性淋巴肉芽肿等，主要通过性接触传播。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 1.治疗原则：(1)明确诊断后应参照卫生部2000年颁布的《性病诊疗规范和性病治疗推荐方案》尽早开始规范治疗。(2)治疗期问禁止性生活。(3)同时检查和治疗性伴侣。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 2.治疗方案：性传播疾病的治疗主要是针对病原微生物的治疗。对梅毒患者，使用青霉素前须进行过敏试验;青霉素过敏者可选用红霉素或多西环素，但妊娠者不宜使用多西环素，对其所分娩的新生儿应采用青霉素治疗;治疗时应注意避免赫氏反应。对淋病患者，必要时可联合应用抗沙眼衣原体的药物。见表3。<br />
