<div>三、盆腔炎性疾病</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 盆腔炎性疾病(PID)是由女性上生殖道炎症引起的一组疾病，包括子宫内膜炎、输卵管炎、输卵管卵巢脓肿和盆腔腹膜炎。性传播感染(sexually transmitted infection，STI)的病原微生物如淋菌、沙眼衣原体是主要的致病微生物。一些需氧菌、厌氧菌、病毒和支原体也参与PID的发生。多数引起PID的致病微牛物是由阴道上行感染的。且多为混合感染。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 1.治疗原则：(1)采集血、宫颈管分泌物和盆腔脓液等标本进行培养及药敏试验。(2)对有发热等全身感染症状明显者，应全身应用抗生素。(3)盆腔炎症大多为混合感染，根据经验选择广谱抗生素覆盖可能的病原微生物，包括淋菌、沙眼农原体、支原体、厌氧菌和需氧菌等。病原微生物检查阳性者依据药敏试验结果调整用药。(4)抗生素的剂量应足够，疗程为14 d，以免病情反复发作或转成慢性。初始治疗时根据病情轻重可静脉给药或非静脉给药;病情好转后可改为口服给药。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 2.治疗方案：药物治疗方案可参见《盆腔炎症性疾病诊治规范(草案)》&uml;1。具体方案包括：头孢替坦(或头孢西丁或其他二代或i代头孢菌素)+多西环素(或米诺环素或阿奇霉素)+甲硝唑;氯林町霉素+硫酸庆大霉素;氟喹诺酮类+甲硝唑(如为莫西沙星，不必加甲硝唑);氨苄西林或舒巴坦+多西环素(或米诺环素或阿奇霉素)+甲硝唑。</div>
