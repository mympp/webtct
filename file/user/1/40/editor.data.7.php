<div>二、宫颈炎</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 急性宫颈炎最常见的致病微生物是淋病奈瑟球菌(淋菌)和沙眼衣原体。均为性传播疾病;也可由葡萄球菌属、链球菌属和肠球菌属引起。</div>
<div>&nbsp;</div>
<div>&nbsp; &nbsp; &nbsp; &nbsp; 1.治疗原则：(1)检测宫颈炎致病微生物，可根据高倍(&times;400)显微镜下官颈涂片每个视野中多形核白细胞&gt;30个，或油镜下可见每个视野多形核白细胞&gt;10个作出初步诊断。(2)治疗期间避免性生活。(3)抗生素的剂量和疗程必须足够。(4)约50%的淋菌性宫颈炎合并沙眼衣原体感染，应同时应用对这两种病原微生物均有效的抗生索。</div>
