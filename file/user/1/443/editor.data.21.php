<p class="MsoNormal">
	&nbsp;2016年4月28日，广州天成医疗技术股份有限公司副总裁李恩、漳州卫生职业学院党委书记林忠出席签约仪式并分别致辞，双方共同签署了《现代学徒制人才培养计划》。<br />
<img src="http://www.tecenet.com/file/upload/201606/15/10174924443.png" alt="" />
</p>
<p class="MsoNormal" style="text-indent:24.0000pt;">
	漳州卫生职业学院是一所全日制公办高等职业学院，创建于1934年，其前身是漳州福音医院护士学校。学校教学教辅机构齐全，设有五系三部两中心，即护理系、临床医学系、药学系、医学技术系、健康与保健系，基础医学部、公共基础部、思想政治理论课教研部，现代教育技术中心、继续教育与培训中心，已形成了以全日制高职高专医学教育为主，本专科函授、各种医学继续教育为辅的办学模式。坚持开放办学理念，积极推进“校企合作，产教融合”人才培养模式改革，专注于培养专业型人才。
</p>
