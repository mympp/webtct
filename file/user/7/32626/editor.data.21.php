<p>CSE是面向21世纪绿色医院建筑节能、环保、智能化、信息化及可持续发展的新特点，涵盖环境规划、手术室净化、核心系统工程、能源环保、新材料应用等整体建设方案各个环节，以科学设计为基础，对手术室工程热点和难点进行探讨，成为政府和卫生系统招标采购的平台，突出医疗技术与建筑设计相结合的时代理念，创造供求一体化产业拓展的合作空间，打造医院环保节能与智能化建设方案，加强医院环保意识和可持续发展，最终实现医院节能建筑、设备选型、工程技术与原材料供应之间的贸易合作，绿色医院建筑装备展，作为中国率先倡导绿色医院建筑与发展的展会及论坛平台，始终以推动中国绿色医院建设的发展与进步为己任，荟萃全球范围内最前沿的产品及技术、为行业提供专业的系统解决方案。</p>
