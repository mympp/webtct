<p>【展会介绍】&nbsp;</p>
<p>&nbsp;</p>
<p>CSE以医院建设中的上下游产业链为中轴线，上届展会吸引了近300家医院建筑设计、智能化管理、绿色建材、节能环保设备、工程配套及综合服务管理的供应商与制造商参会，来自德国、英法、美国、瑞士、瑞典、奥地利、荷兰、以色列、澳大利亚、日本、印度、中国大陆、台湾等国家和地区的展商携带各自的产品和技术亮相，展出面积超过15000平方米，来自20多个国家和地区15176名专业观众，汇聚了一大批行业顶尖技术和品牌厂商，最具有国际化、专业化和科技化，提高绿色医院整体建筑与装备水平，实现基础建设应用领域、工程技术、医疗设备选购的信息交流发挥积极作用，成为政府机构、卫生系统、医院管理者和供应商之间的首选展会。</p>
<p>本届展会是中国医院建筑行业权威机构（中国卫生经济学会医疗卫生建筑专业委员会、中国建筑学会建筑分会医院建筑专业委员会、全国卫生产业企业管理协会卫生材料分会）主办的医疗卫生行业设备选型和集中招标采购的展会，每年9月定期在上海举办。</p>
