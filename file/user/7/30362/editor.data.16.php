<?php exit;?>2014-11-11 15:50:37<p>为临床实验室引进了一个振奋人心的新概念&nbsp;&mdash;&nbsp;将离心涂片机和染色机两个必备的仪器合成一体。在染色循环开始时，血涂片用甲醇和Aerofix固定剂自动固定（可以选择用甲醇或Basofix手工固定）。干燥后，Eosin染色剂被用来染嗜酸的细胞成分，Thiazin染色剂被用于染嗜碱(性)的细胞成分。缓冲清洗可以洗掉过量的染色剂然后用离心法除掉。循环结束时，玻片已经干燥，可以拿到显微镜下观察。选用Cytopro&reg;转子，Aerospray就成为一个超级离心机，可装载最多8片玻片，键盘可编程。增加的成本却只是离心机单机的一小部分。</p>
<p>&nbsp;</p>
<p><strong>特点：</strong><span><br />
</span>当作血液涂片染色机，Aerospray主要具有以下三个优势：<span><br />
</span>节省试剂</p>
<p>试剂经混合后从不同的喷嘴喷到每一玻片上。试剂量根据玻片的数量而定。因此，染色剂的消耗量就减少了，并且试剂的成本也大幅下降。&nbsp;<span><br />
</span>用户自定义编程<span><br />
</span>Aerospray染色机很容易编程。您可以从10个染色强度中选择（包括3个对骨髓染色的设定）。该仪器可满足大部分染色需求。</p>
<p>方便<span><br />
</span>Aerospray染色机自带清洗操作，以使日常维护降到最低限度。该程序简单易行，清洁卫生。&nbsp;<span><br />
</span>高效率&nbsp;<span><br />
</span>Aerospray染色机的处理样品循环周期仅为4分钟，它每小时可处理180多片玻片，因此使大量样品的处理在很短时间内轻松完成。玻片在处理结束后取出时已经变干，可直接拿到显微镜下观察。&nbsp;<span><br />
</span>提高染色稳定性&nbsp;<span><br />
</span>Eosin和Thiazin分别在中性的水溶液中慢速分解，这可延长染色时间。由于染色剂的任何变化发生都比较慢，所以染色结果稳定，重复性高。</p>
<p>无污染</p>
<p>因为染色过程基本不产生沉淀或残余物，所以玻片非常清洁。</p>
<p>自动监测液面&nbsp;<span><br />
</span>染色液液面过低时，会报警提示。</p>
<p>浓缩试剂</p>
<p>所有血液科的试剂都可以选择浓缩液，以便节省成本，少量试剂便可稀释为几升的溶液，更加节省成本</p>
<p>Basofix<span><br />
</span>用水基的染色剂，溶于水的嗜碱性颗粒在染色过程可能会被冲洗掉。但根据经验，脱落的嗜碱性细胞可以很容易地辨认出来。对于比较倾向于颗粒型嗜碱性细胞的用户，Aerospray染色系统还特别提供Basofix固定剂。这种特殊的混合规则产生的染色效果更象醇基染色。固定嗜碱性颗粒的同时，主体颗粒的染色也会得到增强</p>
