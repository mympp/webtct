<div>是一款全自动高压蒸汽灭菌器（steam sterilizer/aotuclave），有单扉和双扉两种型号，本设备适用于各种物品的灭菌，包括工具、衣物、笼具、垫料、液体培养基，琼脂、酵母、玻璃制品、滤器、动物尸体、废弃物（一般性或高危性）等。机器内腔为316Ti不锈钢的带夹套腔体，蒸汽夹套分为一大一小两个夹套系统，可以提高蒸汽的利用率，节省蒸汽，使腔体蒸汽压力更均一,温度更均一。连接蒸汽的为316全不锈钢钢管。门为全自动安全垂直滑动门。两个CPU控制器（主控制器和副控制器），控制更精确，安全性能高。打印机自动打印灭菌过程中的压力，温度变化曲线。配置高输出低噪音西门子二级真空泵，提升泵的性能，节省真空泵耗水量。控制面板：大型液晶触摸屏菜单式控制面板。自带4条标准灭菌程序，同时还包含有BD测试，VT测试机器预热3个程序，并且可以根据用户要求，最多设计14条额外程序可供使用。主机包括纯蒸汽发生器,纯蒸汽指满足EN285质量要求蒸汽。</div>
