<p class="MsoNormal" style="text-indent:22.5pt;">
	继习近平主席发表“中医药是中华文明伟大复兴的先行者”的讲话后，中医药行业迎来了发展的春天。继而，业内又获悉中药配方颗粒政策即将放开，一个上百亿的大市场牢牢吸引住了行业的目光。<span></span>
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	<br />
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	虽然巨大的利润充满了诱惑力，但是在现实面前，国内中药行业仍然面临着生产工艺和装备落后、质量标准和规范体系不够完善、缺乏过程质量控制等问题，这些中药行业发展的桎梏，时刻提醒着制药人——“中药的发展势必要拿出些硬东西才行”。<span></span>
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	<br />
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	2016年<span>4</span>月<span>19</span>日，上海信销信息科技有限公司将在重庆悦来国际会议中心举办第六届固体制剂装备及工艺技术论坛。该论坛响应国家“中医药复兴”的号召，以“中药复兴·设备先行”为主题，凝聚全国顶尖制药设备供应商及全国领军制药企业，从设备着手，共同探索中药复兴之路。<span></span>
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	<br />
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	会议活动中，分别来自浙江天联机械有限公司、常州一步干燥设备有限公司、台州博大制药机械科技有限公司、南宁市飞创能源科技有限公司、朗利维<span>(</span>北京<span>)</span>科技有限公司、天津市三桥包装机械有限责任公司的演讲嘉宾，将从中药生产过程中遇到的实际问题出发，结合自身技术优势，为与会者提供切实可行的问题解决方案。<span></span>
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	<br />
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	此外，有计划进入中药配方颗粒市场的企业，绝不要错过第六届固体制剂装备及工艺技术论坛。<span>4</span>月<span>19</span>日下午，会议论坛环节大咖齐聚，他们将共同讨论“政策放开后，中药配方颗粒的机遇与挑战”。<span></span>
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	<br />
</p>
<p class="MsoNormal" style="text-indent:22.5pt;">
	据透露，为了使论坛环节更加有料，上海信销论坛活动组委会特别邀请了日本资深汉方专家小股和裕先生，他将与现场观众分享日本汉方生产的相关经验，而江西中医药大学副校长杨明先生也会把中国的“高品质中药配方颗粒研制与制造过程关键技术”呈现出来与日本专家进行探讨。<span> </span>
</p>
