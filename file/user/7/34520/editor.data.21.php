<p>&nbsp;</p>
<p><span style="font-family:宋体">不知从何时起，建材行业的老板们也开始赶时髦，变得爱学习了。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">几乎所有开办</span>MBA<span style="font-family:宋体">班、总裁班、精英班、领袖班的高校（如清华、北大、西南财大、西南交大、川大、重大、连纯工科的电子科大也在凑此热闹）创收体系中，都有建材行业的老板们活跃的身影，大家无比积极、万分踊跃地帮助高校内那些心灵活泛、内心空虚、不甘寂寞、自嫌清贫的讲师教授们进行课余创收，让众多&ldquo;人类灵魂的高级工程师&rdquo;们把&ldquo;知识变成资产，知识变成金钱&rdquo;。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">老板们利用周末，告别麻将桌，转向新的课桌、酒桌，通过微信、微博展现自己的高雅追求，炫耀自己和各界大佬的同学关系，这不仅是一种荣光，更是一种时髦。大家阳光积极，热爱学习，希望通过知识第二次改变命运，建立更广泛的人脉资源，使自己的事业更上一层楼，这等追求无可厚非，应该点个大大的&ldquo;赞&rdquo;。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">然而，接触过很多参加了各种总裁班学习的老板后，我们发现了这样一个&ldquo;有趣&rdquo;现象：在参加学习之前，这些建材老板们多半雄心勃勃，壮志凌云，希望学到管理、营销的本领后，立即付诸实施，以求彻底改变自己企业的命运，做强、做大、做上市。但是，学习开始后不久，很多老板的心思就开始发生变化，他们渐渐对自己所从事的实业失去兴趣，悄悄地进行着自己精力的转移。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">对实业不感兴趣，那对什么有兴趣呢？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">建材老板们开始对投资（大部分的所谓投资，实质就是&ldquo;放高利贷&rdquo;）感兴趣。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">财大、交大的若干学院开办的各种总裁班，几乎每个班都成立了类似&ldquo;同学投资会&rdquo;的组织&mdash;&mdash;通常是班主任倡议，班长、副班长（这些职位往往由那些平时爱显摆自己有钱、但又觉得自己的钱总是太少的人担当。当班长、副班长可不是福利，那是要捐钱、花钱的，少则几万，多则几十万，还不谈平时的请客送礼）积极组织鼓惑，面向班上同学集资，然后成立投资管理公司。但是，这样的投资管理公司一般是决不会创办实体的，也不把钱投向实体，而是去放高利贷，或者和银行勾结短期放债（俗称&ldquo;过桥&rdquo;）。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">前几年，祖国山河一片红，经济形势一片大好，房地产业十分兴旺，各种实业尚能赚点钱（虽然还不起利息过高的高利贷，但也能勉强承受），资产也能增值，所以&ldquo;同学投资会&rdquo;很赚钱，参加投资的不少建材老板们都赚到了些钱。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">由于这样的钱来得太容易，所以，很多建材老板的赚钱理念瞬间转换，就对自己原来辛辛苦苦劳累不堪的实业失去兴趣，开始从实业体系中抽出更多资金投向&ldquo;同学投资会&rdquo;，结果，原来的实业就开始走下坡路；又由于老板的精力转移，原有的团队对未来的前途失去信心，激情不再，能量分散，重要人物开始陆续离开，相关合作者如经销商也另觅他枝，公司开始走下坡路。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">这个时候，老板们还不慌，因为&ldquo;东方不亮西方亮&rdquo;，尽管实业公司运作艰难，可&ldquo;高利贷&rdquo;这边还有收获嘛，并且比做实业更快更轻松。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">可是，好日子并不久。今年以来，尤其是最近几个月，当很多投资管理公司倒闭，投资公司的老板、高管纷纷跑路、跳楼，前几天还有个什么联盟（据说是四川最大的&ldquo;同学投资会&rdquo;）破产后，很多老板忽然发现自己血本无归了。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">曾经的几百几千万资产忽然烟消云散，一切又回到起点，坚强一点的人只好&ldquo;欲哭无泪&rdquo;&ldquo;无语凝噎&rdquo;，看有没有机会从头再来，很多同病相怜的人，住在同一个宾馆彼此诉苦，彼此展示伤口，彼此安慰，等待政府抓住破产的&ldquo;坏人&rdquo;后给他们的贪欲一个交代；懦弱的人则只会&ldquo;心灰意冷&rdquo;，从此一蹶不振（温馨提示，请很痛恨也很伤心的亲人最近要加强对他们的陪伴保护）。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">&ldquo;人艰不拆&rdquo;。面对这些曾经经历过大小风光的老板们，这时候有很多话可说但不忍出口，怕打击人，汇成一句劝慰其他人的话：&ldquo;别读&lsquo;总裁班&rsquo;了，小心把你的实业和人生给&lsquo;一总裁掉&rsquo;。&rdquo;</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">对于善于联想的有缘人，提醒您想一想那句话&mdash;&mdash;&ldquo;德不配位，必有&hellip;&hellip;&rdquo;。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p>(<span style="font-family:宋体">二</span>)</p>
<p>&nbsp;</p>
<p><span style="font-family:宋体">读了总裁班后把公司整垮的现象，还有一种类型很普遍，也很有代表性，就是那些用心学了本领，并在实践中应用的人。这是一种矛盾。学以致用，读总裁班，就是为了学来应用的，怎么会因实践应用而把公司整垮呢？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">凡事皆有缘由。我们来具体看看。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">名牌大学的总裁班（其他总经理班、商界精英班等等，都同出一辙）的学员，一般由三类人组成&mdash;&mdash;</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">第一类人是那些像&ldquo;交际花&rdquo;一样的男女，他们进总裁班的主要目的是为了建立人脉，部分人是为了进行伴侣&ldquo;换叫&rdquo;（每次总裁班都会出现这种情况，所以一般大学的总裁班都不会办太久，也不能办太久，最多两年左右，不然，就要附设托儿所了），极少数人是被那些富翁审美疲劳之后扔进来的&ldquo;包袱&rdquo;（参见某些微信段子）。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">第二类人是那些见人就宣讲融资集资然后拿去放高利贷赚大钱</span>,<span style="font-family:宋体">赚轻松钱的人，本文前部分所谈的就是这类人。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">第三类人很可贵，他们是真正来学知识的人。他们非常用心地学习，细细领会，随时谦恭地向老师请教，回去后立即踏踏实实用于实践。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">照道理，第三类人最可爱，应该收获很大。他们自己和别人都这么认为。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">然而，结果很不幸，这类人中，因为运用了从总裁班上学来的知识，结果，不仅无益，还加速、甚至导致了公司的衰败、死亡。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">为什么学了若干教授、博士等高级知识分子所传授的，先进的战略规划、模式构建、企业管理、市场营销、团队建设等知识，并且还到诸多优秀的标杆企业如海底捞、阿里巴巴等观摩学习过后，回来还把公司搞垮了呢？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">道理其实很简单：</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p>1<span style="font-family:宋体">、那些没有任何实践经验、空洞的理论知识十分渊博的教授博士们，所传授的书本知识，来源于那些大企业（很多是跨国集团）的运作策略，而且是过去式（书本上所选案例几乎都是特定时代、特定背景下的过时案例，甚至今天还有很多学习材料仍然在把诺基亚、索尼等衰败企业作为学习典范）。而参加总裁班学习的学员，几乎都是小微型企业的老板。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">你本身只是一个蚊子、蟑螂的体量，却要学饲养大象、肥猪的方法，用饲养大象的方法来养蟑螂，不死才怪（并非脑筋急转弯：鱼缸里面的鱼是怎么死的？标准答案：撑死的）。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">再者，时代也不同了，尤其是，互联网时代来了&hellip;&hellip;</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p>2<span style="font-family:宋体">、有人说，在总裁班，我们还是学了&ldquo;海底捞&rdquo;&ldquo;小米&rdquo;&ldquo;胖东来&rdquo;等实用性很强的案例啊，这应该很有效嘛。我想说，不要被那些教授博士忽悠了，他们自己都没有搞懂&ldquo;海底劳&rdquo;&ldquo;小米&rdquo;为什么成功，而胖东来正在经历死亡危机。请问，您的老师（叫兽们）教您学&ldquo;海底捞&rdquo;的什么？有人说，学&ldquo;服务&rdquo;。这听起来似乎有理，也很好模仿，照着做就行。可是，为什么很多年了，直到今天，全国都还没有一家学&ldquo;海底捞&rdquo;成功的？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">你只看到海底捞的服务好，却不知道，海底捞为什么会提供这样的服务？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">这涉及到企业灵魂的&ldquo;基因&rdquo;问题，也就是理念的问题。你没有那个基因，没有那个理念，你是无法做到的，即使一时心血来潮做到，也不能坚持太久。</span></p>
<p>&nbsp;</p>
<p><span style="font-family:宋体">所以说</span>,<span style="font-family:宋体">模式容易照搬，但是人家为什么会推出这种模式的动机，如果你不懂，你是学不会的；即便懂了，你没有那个理念，仍然做不久。如果一定要学，结果要么是东施效颦，要么是邯郸学步，很悲戚。</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p>3<span style="font-family:宋体">、真正的战略战术，本身并不是什么伟大的问题，有了发自心底的、真正的对顾客的&ldquo;爱&rdquo;，这一切自然会迎刃而解，各种灵感都会蜂拥而至，你不需要刻意去学什么。就是说，爱，是最大的技巧，有了爱，不需要学什么技巧。可是，现在的总裁班，专门教你舍本逐末，学习各种奇技怪巧，你怎么不踏入歧途，走上邪路，最后不归？</span></p>
<p><span style="font-family:宋体"><br />
</span></p>
<p><span style="font-family:宋体">别了，创收能力很强的总裁班！别了，不甘寂寞的名牌大学的教授们！</span></p>
