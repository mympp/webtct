    <strong><span style="font-family: -apple-system-font;color: rgb(0, 0, 0);letter-spacing: 0;font-size: 24px">谈话时，让别人舒服的程度，决定了你的高度</span></strong>
</h2>
<p>
    <span style=";font-family:Calibri;font-size:14px">&nbsp;</span>
</p>
<p style="margin-top:0;margin-right:0;margin-bottom:0;margin-left:0;padding:0 0 0 0 ;line-height:26px">
    <span style=";font-family:Calibri;font-size:14px">&nbsp;<img src="http://www.weixinbianjiqi.com/attachments/20160429/20160429161957_953.png" wx-src="undefined"/></span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">职场上，有这样两种截然相反的人。有人生怕别人舒服，尽量让别人不舒服，而只要自己舒服就行。还有一类人生怕别人不舒服，尽量让别人舒服，哪怕委屈自己。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;color: rgb(61, 167, 66);letter-spacing: 0;font-size: 20px">让别人是否舒服直接影响你的成功</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">当你和不同的老总接触，有几十万年薪的，也有几百万的，甚至有过千万级年薪的，要问对这些老总有什么本质感觉上的不同，回答是，越是高薪的老总在与其交往中他会越让你感觉到舒服。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">跟千万年薪的老总谈，谈上两到三个小时，无论你说的话是酸甜苦辣等味道，他们都能把每一句话平缓接起来回答，而从不让一句话落地或磕碰，让人感觉非常舒服。就像打太极，无论什么招式，全部是以柔克刚。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">这就是高手过招，化解问题于无形之处，于无声之中。他们之所以挣千万年薪，自有千万年薪的价值，让人舒服程度也许就是一个衡量指标。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">常常发现越是与年薪水平低的人交流越容易让人不舒服</span></strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">。回想平时大家之间的沟通交流，磕磕绊绊，到处充满着不舒服的感觉。你不让别人舒服，别人就会让你不舒服。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">想提高年薪吗？就从如何让别人舒服着手，提升这方面的品质和修养。我们平常说话两个人都容易伤害到对方，引起争执。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">曾看到一位级别很高的70多岁的老人和人交谈，他的每一句话都不会伤及到任何一个人，不会让周围的任何一个人感觉不舒服。在一起聚餐十多人，每一句话都能照顾到所有的人，无论男女老少都感觉舒服，这是何等的修养。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">战争年代就是千方百计把敌人消灭掉，想法让敌人不舒服。在和平建设年代，你让别人舒服的程度，决定着你成功的程度。</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">你是否让别人舒服，直接影响着你的成功。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;color: rgb(61, 167, 66);letter-spacing: 0;font-size: 20px">创造自我，追求无我</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">李嘉诚请马云吃饭的故事是一个很好的证明：</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">长江CEO班有30几个同学，包括马云、郭广昌、牛根生等国内大家认为很了不起的人。有一次，班上组织我们去香港见一次李嘉诚，他可谓华人世界的超级大哥了。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">没见面之前，心里有个情景假定，比如约会衣服要穿整齐等，当时我就想：见老大哥相当于见领导，一般我们见这种人，可能第一见不到大哥先见到椅子、沙发；第二伟大的人来了，我们发名片人家不会发名片；第三人家跟你握手然后你站着听讲话，就像我们被接见，在人民大会堂听讲话我们鼓掌就完了；最后吃饭肯定有主桌，大哥在那坐一下，吃两筷子说忙先走了；然后我们很激动回来写感想……</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">结果这次见面完全颠覆了之前的想法。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">首先电梯一开，长江顶楼，70多岁的大哥站着跟我们握手，这样的开场很不一样，我有点愣。其次，一见面大哥先发名片，这个也很诧异，而且发名片还给你递过来一个盘子，递盘子干吗？</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">抓阄，盘子里有号，拿名片顺便抓个号，这个号决定你吃饭的时候坐哪桌，避免到时候我们这些同学为谁坐1号桌，谁坐2号桌心里有想法。后来才知道，照相也根据这个号，站哪就是哪。我觉得挺好，大家避免尴尬。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">站好之后我们小人物的能力出现了，我们就鼓掌希望大哥讲话，大哥说没准备讲话，但这时候大哥不讲我们小人物角色演不下去，所以必须让他讲，这个经历经常有，最后大哥说，我没有准备，我只讲八个字叫做&quot;创造自我，追求无我&quot;。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">这一听大哥读书很多，学历不高读书很多，讲的都是哲学，&quot;创造自我，追求无我&quot;，讲完了普通话又用广东话讲一遍，之后发现还有老外用英文再讲一遍，就讲这八个字讲完了我们体会这话里的深意。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;color: rgb(61, 167, 66);letter-spacing: 0;font-size: 20px">什么叫追求自我？</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">你在芸芸众生中，把自己越做越强大，自我膨胀，超越别人，这个过程就容易给别人以压力。因为你强大了以后很强势，就像你老站着，别人蹲着，别人就不舒服。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">所以你要追求无我，让自己化解在芸芸众生中，不要让别人感觉到你的压力。一方面创造自我，一方面让自己回归于平淡，让自己舒服也不给大家制造压力。</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">听完讲话我们开始鼓掌，然后开始吃饭。我运气不错，抽到了跟大哥一桌，我当时想，和大哥挺近的，这样吃饭可以多聊一会儿，所以开始没着急说话，没想到吃十几分钟的时候大哥站起来说抱歉要到那边坐一下。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">这时我们才发现，四张桌子，每个桌子都多放了一副碗筷，他每个桌子都坐。一个小时的吃饭时间，他四个桌子轮流坐，而且几乎都是15分钟，到这时，大家都被大哥周到和细致的安排感动了。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">大哥大概每个桌子转完基本也就结束了，结束之后他没先走，逐一跟大家握手，在场的每个人都要握到，墙角站着一服务员，大哥专门跑到那和他握手。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">这时候我想起看过他的一个演讲，问他们有没有关于这个的书，当时没准备，他交代下面一下，结果下车的时候那个书就送到我手里了。整个过程让我们每个人都很舒服。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <strong><span style="font-family: 宋体;color: rgb(61, 167, 66);letter-spacing: 0;font-size: 20px">无形的软实力</span></strong>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">这就是大哥之所以成为大哥的原因，这就是他的软实力。他具有一种看不到的能力，这个能力是价值观，用他的话说就是追求无我，他让每个人都舒服。后来我跟我们班班长提到这事，他说老先生就是因为做人周到真诚，所以很多人到了香港都愿意和他做生意。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">你不能说李嘉诚做到这些是因为有钱，李嘉诚是有钱，但这不是软实力的来源，有钱只是现状，他创业的时候比他有钱的人肯定多，他不是最有钱的。比尔·盖茨创业的时候比他有钱的也很多，为什么到现在当时比他们有钱的人看不见了？所以钱是不能决定成败的，是钱以外的东西决定谁能赶超过谁，谁能挺到最后。所以，</span><strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">钱以外的东西才是软实力</span></strong><span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">。</span>
</p>
<p style="margin: 6px 0 15px;text-indent: 24px;padding: 0;line-height: 32px">
    <span style="font-family: 宋体;letter-spacing: 0;font-size: 18px">李嘉诚软实力的来源就是钱以外的东西，是他做人做事的方法，即“创造自我，追求无我”的价值观。这个价值观不是说出来的，而是做出来的，你们感受到了，会出去讲，这样慢慢就形成一个场，形成这个场子之后大家都愿意和他做生意，把最好的机会给他，于是他越来越成功，这就形成了软实力。</span>
</p>
