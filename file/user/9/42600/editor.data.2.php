	<span>2016年3月5日上午，由广东省养生文化协会专家委员会组成的“名医工作室”正式挂牌成立并对外运营。</span> 
</p>
<p style="text-align:left;">
	“名医工作室”的宗旨是：传播养生文化，提供疑难杂症咨询服务，打造健康身心；为精英量身定做，提供高端、优质的个性化服务； 架设医生与客户之间的服务平台；传播医学知识，感受中医药文化的魅力，共同创造美好生活。
</p>
<p style="text-align:left;">
	“名医工作室”负责人陈晓华在接受采访时说
</p>
<p style="text-align:left;">
	“名医工作室的宗旨用一句话概括起来就是：您的健康 ，是我们的追求！”
</p>
<div style="text-align:left;">
	<span>“名医工作室”位于广州市白云区远景路168号时代商业中心B幢502室（020－36683509）。</span> 
</div>
<div style="text-align:left;">
	<span>据悉，“名医工作室”目前在库专家已有近百位，这些专家，有的来自著名的公立医院、有的来自民间传统医学世家。每日会邀请一部分专家来工作室提供咨询服务，也会根据客户的需要，一对一地约请专家提供单独的个性化服务方案。</span> 
</div>
<p style="text-align:left;">
	工作室着重于医、患之间的沟通、诊疗咨询，实行医、药分家，即只提供医术服务，不提供药品服务。客户咨询，需提前预约，有需要运用公医或社保的客户，可回医生所在医院按原来程序完成流程。
</p>
<p style="text-align:left;">
	据了解，去年，广州中医药大学联合广东省宋庆龄基金会等单位联合发起并成立了广东省养生文化协会，在此基础上组建了专家委员会，建立了我省中医药名医资料库，目前已拥有上百名知名中医专家。
</p>
<p style="text-align:left;">
	“名医工作室”的专家来自于该名医资料库。为促进传统中医药与现代医学结合
</p>
<p style="text-align:left;">
	“名医工作室”在立足于中医的基础上，也重视西医技术，工作室也会有著名西医提供咨询服务。随着业务的不断拓展，工作室专家队伍将越来越大。
</p>
<p style="text-align:left;">
	据了解，“名医工作室”这种形式，符合国家大力鼓励发展中医药的政策，是医疗服务多元化的一种有效探索。
</p>
