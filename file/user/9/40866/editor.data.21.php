<p style="text-indent:2em;">
	<strong><span style="font-size:16px;">圣多宝消毒机</span></strong> 
</p>
<p style="text-indent:2em;">
	麻醉机、呼吸机内管回路消毒机是一台移动式消毒设备，在不拆卸机器状态下，对机器内管回路进行完整消毒，保障了麻醉机、消毒机使用质量和寿命；<br />
◆消毒机移动方便、操作简便、消毒时间短；<br />
◆精确注液、自动消毒，避免医护人员接触消毒剂、不污染环境，保障安全：<br />
◆避免建设消毒室高额费用，经济合理；
</p>
<p style="text-indent:2em;">
	<strong><span style="font-size:16px;">一、工作原理</span></strong> 
</p>
<p style="text-indent:2em;">
	“圣多宝”牌消毒机采用O3和H2O2&nbsp;共同作用机理，协同增效，消毒杀菌，同时降低各单位因子原来的毒性、刺激性、腐蚀性等，通过气泵将O3和H2O2&nbsp;雾化气体导入麻醉机、消毒机内管回路，杀死内管回路中的细菌。
</p>
<p style="text-indent:2em;">
	<strong> 二、工作流程图</strong> 
</p>
<p style="text-indent:2em;">
	精确注液→电压雾化、臭氧发生→环保消毒→残余分解→恒温干燥→回收残余→即可使用
</p>
<p style="text-indent:2em;">
	<strong> 三、适用范围</strong> 
</p>
<p style="text-indent:2em;">
	◆&nbsp;&nbsp;适用于医疗机构的麻醉机、呼吸机内部管路消毒灭菌。
</p>
<p style="text-indent:2em;">
	◆&nbsp; 适用于医疗机构使用的其他内管回路设备消毒灭菌。
