<p>
	由国家科技部、国家发展改革委、财政部等18个部门机构共同举办的国家“十二五”科技创新成果展，于6月1日-7日在北京展览馆举办。在这场国家最高级别的科技创新大展上，都有哪些医疗器械产品得以亮相呢？
</p>
<p>
	<br />
</p>
01<strong><strong>天智航骨科机器人</strong></strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibE4WYLDsdk4BOaa5IaHXvlmNzyLJVqgeXPaXacKD4bMnRLLp6ic5iayDxg.jpeg#" height="auto" width="320" class="" />
</p>
<p style="text-align:center;">
	<br />
</p>
<p>
	由北京积水潭医院、北京航空航天大学、北京天智航公司、中国科学院“医工企”联合研发的骨科手术机器人，是国际上唯一能够开展四肢、骨盆骨折以及脊柱全节段（颈椎、胸椎、腰椎、骶椎）手术的骨科机器人系统。
</p>
<p>
	<br />
</p>
<p>
	该系统在多模图像配准、机器人控制、患者实时跟踪和路径自动补偿等关键技术实现重大突破。通过骨科手术机器人开展手术，精确度达到0.8毫米，减少术中辐射70%以上，提高手术效率20%以上，并具有贾少失血量和术中组织创伤等优势。
</p>
<p>
	<br />
</p>
<p>
	目前，该系统已在北京积水潭医院等十多家医院使用，累计手术超过2000例。
</p>
<p>
	<br />
</p>
02<strong>安翰胶囊胃镜机器人</strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibEZ1GB1iaVVNbJp4j2KnnQ0AIu42QEcYSEBGbHfcPI0AlIKQ92GPNqSwA.jpeg#" height="auto" width="320" class="" />
</p>
<p>
	由安翰公司研发生产的“胶囊胃镜机器人”，被医学界誉为“21世纪胃疾病检查和内镜发展的革命性创新”。
</p>
<p>
	<br />
</p>
<p>
	胶囊胃镜不同于以往的插管胃镜，只需喝一口水，吞下一粒普通胶囊大小的胃镜机器人，这颗“有眼有脚”的小小机器人，就会在人体内“翻转腾挪”，360度无死角地观察胃部所有部位，而受检者毫无不适感，只需15分钟即可完成胃镜检查。
</p>
<p>
	<strong></strong>
</p>
03<strong>品驰脑起搏器、迷走神经刺激器</strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibE9fp91CkR0BCoH4OT6lm5Dib4TJ6CFjJCg6TwcibbU115814FMw8HMx9g.jpeg#" height="auto" width="320" class="" />
</p>
<p>
	清华研制、品驰制造的脑起搏器（DBS）打破美国技术垄断，获批投产上市近三年时间内为帕金森病及运动障碍疾病患者带来巨大帮助，总植入量近4000例次，累计为患者节支近3亿元。
</p>
<p>
	<br />
</p>
<p>
	而由该团队研发、主要应用于难治性癫痫治疗的迷走神经刺激器（VNS）也在近期获批准产上市，再次打破该项技术依靠进口昂贵设备、难于推广的难题，为我国难治性癫痫患者带来希望。
</p>
<p>
	<br />
</p>
04<strong>联影动态多极3.0T MR与世界首台96环光导PET-CT</strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibENaTSEMBsaqLHrKct0aLXzP621bY3RiccLNjebYl09mnR8xGt6VuvVFw.jpeg#" height="auto" width="320" class="" />
</p>
<p>
	联影推出的中国首台动态多极3.0T磁共振，实现了磁体、梯度、射频等全部核心部件的自主研发，并在此基础上自主研发了全面的临床和科研应用，一举打破<span style="line-height:1.6;">高场磁共振领域外资技术长达数十年的垄断，其创新性动态磁场调节技术和多极射频发射技术为业界首创，用于解决高场磁共振成像中最困难的B0场和B1场不均</span><span style="line-height:1.6;">匀问题，实现了因人而异、精准调节的定制化成像。</span>
</p>
<p>
	<span style="line-height:1.6;"><br />
</span>
</p>
<p style="text-align:left;">
	世界首台96环光导PET-CT则以业界最高灵敏度、业界最高分辨率及业界最大轴向视野填补国际空白，代表世界核医学最先进水准，实现精准定量化诊断。以分辨率为例，它凭借一体化光导探测器技术，实现2.8毫米NEMA分辨率，较目前市场主流临床PET-CT最高分辨率提升一倍，大大提高微小肿瘤早期诊断的几率。此外，欧盟CE认证和日本JFDA认证又赋予它通行欧亚大陆的威力。
</p>
<p>
	<br />
</p>
05<strong>海信计算机辅助手术系统及外科智能显示系统</strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibEibMalnrkbK9NgglSTDLeZf0w4Nic5BBe6qxMEibPxYlW31oEy8lJNuXVw.jpeg#" height="auto" width="320" class="" />
</p>
<p>
	“海信CAS计算机辅助手术系统”和海信外科智能显示系统由青大附院科研团队与海信专家团队共同研发，能够将肝脏病患的CT数据变成三维的数字肝脏以指导临床手术，在手术前从不同视角进行全方位评估、规划和设计手术方案。这套系统解决了外科临床手术需求的痛点，实现了数字化精准医疗。
</p>
<p>
	<br />
</p>
06<strong>众创空间的儿童视知觉治疗系统</strong>
<p style="text-align:center;">
	<img src="http://www.tecenet.com/file/upload/weixin/image/cZcu8wCxhdFnRz6T5o5MLzc8jAy0QEibEpa5Laiaia7RibVnHyGZD4JWNtKrRgHLL4eaMiaKYK8ia1jy8ty4VqPsCUVA.jpeg#" height="auto" width="320" class="" style="width:100%;height:auto;" />
</p>
<p>
	展会上，位于滨海新区的天津国际生物医药联合研究院TjAb众创空间（以下简称“TjAb众创空间”），作为天津市首个生物医药类的众创空间携10余家入驻企业代表，在展会上通过实物、展板、模型等形式展示了本市生物医药领域的创新创业进展和最新成果。
</p>
<p>
	<br />
</p>
<p>
	TjAb众创空间参展内容包括儿童视知觉治疗系统、功能食品系列产品、闭环式生态大健康系统等多个主题。
</p>
