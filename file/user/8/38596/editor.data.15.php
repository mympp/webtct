<p style="text-indent:28.9pt;background:white;" class="MsoNormal">
	“世界健康产业大会”（<span>World Health Industry Conference </span>英文缩写：<span>WHIC )</span>是中国卫生部医疗保健国际交流促进会、中国保健营养理事会、中国国际健康产业博览会组委会、美国美中经济贸易促进会、美国传统中医药协会、美国美中保健品协会、俄罗斯亚洲商务合作中心、俄罗斯莫斯科工商会、英国健康协会、日本日中商业交流会、德国天能生命科学研究院、国际企业联合会、韩国有机农业协会、亚非绿色健康产业与环保文化合作委员会、卡翁达非洲儿童基金会等多个国家相关机构共同发起的健康产业国际性会展活动。世界健康产业大会每年定期（世界卫生日期间）在中国北京召开，由国际论坛、产品博览、公益活动三大部分组成，被誉为全球健康产业的最高级别盛会。<span></span> 
</p>
<p style="text-indent:28.9pt;background:white;" class="MsoNormal">
	首届世界健康产业大会已于<span>2012</span>年<span>4</span>月<span>7</span>日—<span>9</span>日在中国北京成功举办，中国全国政协主席贾庆林、德国总理默克尔、新加坡总统陈庆炎、世界卫生组织代表蓝睿明等国内外政要和团体纷纷作为嘉宾出席或来电祝贺。第二届已于<span>2013</span>年<span>4</span>月<span>7</span>日—<span>9</span>日在中国国家会议中心隆重召开，中国全国人大副委员长周铁农、西班牙前首相萨帕特罗、柬埔寨首相洪森、世界卫生组织总干事陈冯富珍等多国政要、领导专家和500多位企业家共同参与了本次盛会。与会三天共有来自美国、俄罗斯、德国、英国、加拿大、意大利、土耳其、西班牙、巴西、印度、沙特、日本、韩国、澳大利亚、柬埔寨、巴基斯坦等近40多个国家的政经领袖和业界精英汇聚北京，和48500人次的博览会观众共同见证了世界健康产业大会的再次演绎。
</p>
<p style="text-indent:28.9pt;background:white;" class="MsoNormal">
	2016第五届世界健康产业大会由中国商务部批准，多个部委支持，众多国际组织鼎力协助，预计论坛代表将突破<b><span>50</span></b>个国家和地区，企业家人数至少<span>500</span>人。博览会各国参展商将接近两千家，参展观众将突破<span>6</span>万人次，将成为又一次促进人类健康事业发展的重要大会。
</p>
