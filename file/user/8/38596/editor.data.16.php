	<span style="font-size:14pt;"><span style="font-weight:700;">四、产品特点</span></span> 
</div>
<div style="font-family:'Hiragino Sans GB', Tahoma, Arial, 宋体, sans-serif;color:#333333;font-size:12px;" class="tinymce-module fd-clr">
	<p class="p0">
		<span style="font-size:12pt;"><span style="font-weight:700;">1</span><span style="font-weight:700;">．硬度高、重量轻</span>：聚氨酯材料构成有软链段和硬度段，而软链段使其内聚力增高，硬度增强。经检测固化后的迈时捷高分子绷带硬度是传统石膏的20倍，这一特点保证了正确复位后可靠牢固的固定作用。固定用材少，重量轻，相当于石膏重量的1/5，厚度的1/3，可使患处负重小，对固定后功能锻炼减轻了负荷，有利于血液循环，促使愈合。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;">其次，石膏凝固后，仍然具有一定的脆性，因此负重过大时，可引起断裂，导致骨折整复后的再移位，而迈时捷高分子绷带的聚氨酯材料构成有软链段和硬度段，这个特点有效降低患部受外力作用而再次损伤的可能性，从而有效地保障了固定的作用。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;">注：同一部位的固定，迈时捷高分子绷带的用料比石膏最少节约1/3。如前臂管型，石膏绷带用量3卷，迈时捷高分子绷带用量1卷。下肢管型，石膏绷带用量25卷，迈时捷高分子绷带用量5卷。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;"><span style="font-weight:700;">2.&nbsp;</span><span style="font-weight:700;">多孔、良好的透气性</span>：迈时捷高分子绷带使用了高质量的原纱，独有的网状编制技术，具有良好的透气性，有利于皮肤透气，解决了局部管式包扎所致的皮肤潮热、瘙痒等问题。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;"><span style="font-weight:700;">3</span><span style="font-weight:700;">．硬化速度快</span>：迈时捷高分子绷带硬化过程快，在打开包装后2-3分钟开始硬化，20分钟就可以承重了，而石膏绷带需24小时左右才能完全硬化承重。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;"><span style="font-weight:700;">4．极好的X</span><span style="font-weight:700;">线透射性</span>：迈时捷高分子绷带及高分子夹板对放射线的通透性极佳，X线效果清晰，有利于医生在治疗过程中，随时可以了解患肢的愈合情况。而石膏的透射性比较差，有时只有去除固定后，才能清楚的了解愈合情况。因而避免了有时在石膏拆除后通过X线检查发现未达到愈合标准，而需要二次重新包扎的麻烦。</span> 
	</p>
	<p class="p0">
		<span style="font-size:12pt;"><span style="font-weight:700;">5</span><span style="font-weight:700;">．良好的防水性</span>：迈时捷高分子绷带硬化后，表面光滑，对水分的吸收率比石膏和一般玻璃纤维制成的绷带和夹板低85%，即使患肢接触水的环境后，也能有效的保证患部干燥。亦不怕二次浸水，可以佩戴绷带进行沐浴和水疗。</span> 
	</p>
