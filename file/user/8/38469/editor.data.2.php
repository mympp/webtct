</p>
医械专项整治一直都是国家、各省、市、县食药监局工作中的重中之重。据不完全统计，在今年公开的信息里，全国至少有四个省开展了医疗器械专项整治。
<p>
	<br />
</p>
<strong>四省整治情况</strong> 
<p>
	<strong><span style="color:#ff0000;">广东：</span></strong>2015年9-10月广州省局对医疗器械经营企业组织了飞行检查的工作，现已全面完成，有20家医械企业不合格，其中已有三家被注销，其余或整改或停业整顿。
</p>
<p>
	<br />
</p>
<p>
	<span style="color:#ff0000;"><strong>山东：</strong></span><span style="line-height:1.6;font-size:16px;">据悉，山东省开展了医疗器械专项整治2015行动。在专项行动中，山东省各地共立案859起、结案766起、<span style="background-color:#ffffff;color:#3e3e3e;">移送公安机关涉嫌犯罪案件2起。</span>罚没款2138.67万元。</span>
</p>
<p>
	<span style="line-height:1.6;font-size:16px;"><br />
</span>
</p>
<p>
	<strong><span style="line-height:1.6;color:#ff0000;font-size:16px;">陕西：</span></strong>在陕西省食药监局公开的9起典型案例中，其中有三件案件涉及医疗器械行业，其中涉及13家医疗器械企业，其中12家械企被移送公安。<span style="line-height:1.6;font-size:16px;"><br />
</span>
</p>
<p>
	<br />
</p>
<p>
	<span style="color:#ff0000;"><strong>湖北：</strong></span>截至目前，全省共检查医疗器械生产经营单位11429家，责令限期改正920家，立案查处158家；检查装饰性彩色平光隐形眼镜和避孕套生产、经营、使用单位9448家，警告636家，责令整改221家，责令停业13家，查处游商小贩22家，无注册品种2个，无照经营50家，处理举报投诉11起。
</p>
<p>
	<br />
</p>
<strong>五类医械或成2016专项整治重点</strong> 
<p>
	<span style="color:#ff0000;"><span style="color:#000000;">2016年即将来临，医械圈专项整治的力度不会减小反而会加强，日后这五大类医械会将成为专项整治重点。</span></span>
</p>
<p>
	<span style="color:#ff0000;"><br />
</span>
</p>
<p>
	<span style="color:#ff0000;">无菌类医械</span>：重点检查生产环节：采购过程、洁净室控制、灭菌过程、生产过程、产品可追溯性、出厂检验等是否符合要求。
</p>
<p>
	<span style="line-height:1.6;font-size:16px;"><span style="line-height:1.6;color:#ff0000;font-size:16px;"><br />
</span></span>
</p>
<p>
	<span style="line-height:1.6;color:#ff0000;font-size:16px;">植入性医械：<span style="line-height:1.6;color:#000000;font-size:16px;">重点检查</span></span><span style="line-height:1.6;font-size:16px;">经营环节：购销渠道是否合法，进货查验记录和销售记录是否真实完整，运输、储存条件是否符合要求等；使用环节重点检查：产品购进渠道是否合法，是否使用不合格产品，购进、验收、使用记录是否齐全等。</span>
</p>
<p>
	<span style="line-height:1.6;font-size:16px;"><br />
</span>
</p>
<p>
	<span style="color:#ff0000;">避孕套：<span style="color:#000000;">重点检查流通环节和生产环节：</span></span><span style="line-height:1.6;font-size:16px;">降低生产条件、擅改标准、擅改工艺违规生产、未经检验或未按规定检验即放行产品等行为；：无证经营、经营无证产品行为等。</span>
</p>
<p>
	<span style="line-height:1.6;font-size:16px;"><br />
</span>
</p>
<p>
	<span style="color:#ff0000;">平光隐形眼镜</span>：重点检查经营环节：是否有无证及超范围经营行为，产品有无《医疗器械注册证》等资质证明材料，产品进货渠道是否合法，是否销售不合格产品<span style="line-height:1.6;font-size:16px;">，是否建立并执行不良事件报告制度，进口产品说明书、标签和包装标识是否使用中文等。</span>
</p>
<p>
	<span style="line-height:1.6;font-size:16px;"><br />
</span>
</p>
<p>
	<span style="line-height:1.6;color:#ff0000;font-size:16px;">体外诊断试剂：<span style="line-height:1.6;color:#000000;font-size:16px;">IVD领域日后将会是重点，重点检查是“测定范围项”、“线性范围项”和“特异性项”三大项。</span></span>
</p>
