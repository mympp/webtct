<?php exit;?>2014-03-24 10:37:25中国医疗器械行业协会会长赵毅新日前在山东威高集团举行的;医疗器械生产企业开放日活动;上介绍，我国已成为继美国和日本之后，全球第三大医疗器械市场。2013年，我国医疗器械行业产值3400亿元，生产企业近16000家。但我国医疗器械产业在国际上处于中等偏下水平，技术创新及转化能力薄弱，关键零部件依赖进口，高端产品仍以仿制、改进为主。
<div>&nbsp;</div>
<div>　　医疗器械生产企业开放日活动，由国家食品药品监管总局组织，旨在增强公众对国产医疗器械质量安全的了解和信心。</div>
<div>&nbsp;</div>
<div>　　食药监总局医疗器械监管司副司长孙磊表示，我国将对国内医疗器械企业实施兼并和整合，提高该行业整体的质量控制水平和研发水平。食药监总局日前也已出台规定，对创新医疗器械实施特别审批程序。此外，新修订的《医疗器械监督管理条例》即将实行，对我国医疗器械的生产规范和质量安全提出了更严格的要求。</div>