<div>这是因为，植物中的各种色素都具有相当出色的健康价值，特别是强大的抗氧化作用。而色素较高的植物，其抗病性往往更强，营养成分也更为出色。</div>
<div>&nbsp;</div>
<div>人们热爱鲜艳浓重颜色的特性，很可能正是来自一种追求健康的遗传本能。美丽的颜色证明它的新鲜，以及它的抗氧化性等性质；美好的味道，昭示着食品中丰富的营养成分。所以，在选择天然状态食物的时候，我们完全有理由大呼&ldquo;好色有理&rdquo;&mdash;越浓烈，越鲜艳，越健康。只是，在选择加工食品的时候，我们不妨少点好色心情，选择那些颜色更为朴素的产品，也许更符合健康的需要。</div>
